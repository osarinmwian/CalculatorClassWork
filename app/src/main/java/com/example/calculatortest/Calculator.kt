package com.example.calculatortest

class Calculator{
    fun addition(num1:Int, num2:Int): Int{
        return num1 + num2
    }
    fun substraction(num1:Int, num2:Int): Int{
        return num1 - num2
    }

    fun multiplication(num1:Int, num2:Int): Int{
        return num1 * num2
    }
    fun division(num1:Int, num2:Int): Int {
        return num1 / num2
    }
}
